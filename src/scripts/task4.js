/* 
Класс Calculator: 
Реализуйте класс Calculator, 
который будет иметь методы для основных математических 
операций: add, subtract, multiply, и divide. Каждый метод 
будет принимать два числа и возвращать результат соответствующей 
операции. 
*/

class Calculator {
  static add(num1, num2) {
    return num1 + num2;
  }
  static substact(number1, number2) {
    return number1 - number2;
  }
  static multiply(el1, el2) {
    return el1 * el2;
  }
  static divide(element1, element2) {
    return element1 / element2;
  }
}
